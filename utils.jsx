export default function fixNames(lan) {
    if (lan === "ar")
        return "Arabic"
    if (lan === "de")
        return "German"
    if (lan === "en")
        return "English"
    if (lan === "es")
        return "Spanish"
    if (lan === "fr")
        return "French"
    if (lan === "he")
        return "Hebrew"
    if (lan === "it")
        return "Italian"
    if (lan === "nl")
        return "Dutch"
    if (lan === "no")
        return "Norwegian"
    if (lan === "pt")
        return "Portuguese"
    if (lan === "ru")
        return "Russian"
    if (lan === "sv")
        return "Swedish"
    if (lan === "ud")
        return "Urdu"
    if (lan === "zh")
        return "Chinese"
}